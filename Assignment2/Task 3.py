#input
print(f"Please enter the length and the width of a rectangle.")
length = float(input("Enter length of the rectangle:"))
width = float(input("Enter width of the rectangle:"))
print()

#calculations
perimeter = 2 * (length + width)
area = length * width

#output
print(f"The total perimeter of the rectangle is {perimeter}.")
print(f"The area of the rectangle is {area}.")

